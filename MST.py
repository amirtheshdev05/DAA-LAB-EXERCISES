# Driver Code
# -----------------------------
n = 7

edges = [
    (7, 0, 1),
    (5, 0, 3),
    (8, 1, 2),
    (9, 1, 3),
    (7, 1, 4),
    (5, 2, 4),
    (15, 3, 4),
    (6, 3, 5),
    (8, 4, 5),
    (9, 4, 6),
    (11, 5, 6)
]

# Build adjacency list for Prim's Algorithm
adj = {}

for weight, u, v in edges:
    adj.setdefault(u, []).append((v, weight))
    adj.setdefault(v, []).append((u, weight))

# Run Kruskal's Algorithm
kruskal_mst, kruskal_cost = kruskal(n, edges.copy())

# Run Prim's Algorithm
prim_mst, prim_cost = prim(n, adj)

# -----------------------------
# Output
# -----------------------------
print("=== Kruskal's MST ===")
for u, v, weight in kruskal_mst:
    print(f"Edge ({u} - {v}) Weight: {weight}")
print("Total MST Cost:", kruskal_cost)

print("\n=== Prim's MST ===")
for u, v, weight in prim_mst:
    print(f"Edge ({u} - {v}) Weight: {weight}")
print("Total MST Cost:", prim_cost)